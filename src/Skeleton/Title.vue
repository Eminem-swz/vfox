<template>
  <div class="fx-skeleton-title" :class="{ animated: useAnimation }"></div>
</template>

<script>
export default {
  name: 'fx-skeleton-title',
  inject: {
    skeletonSubOptions: {
      default: null
    }
  },
  props: {
    // 是否显示动画
    animated: {
      type: Boolean,
      default: false
    }
  },
  computed: {
    useAnimation() {
      if (this.skeletonSubOptions) {
        return this.skeletonSubOptions.animated
      }
      return this.animated
    }
  }
}
</script>
